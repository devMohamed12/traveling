if (window.location.href.includes("country")) {
  // Code for country.html
  const heroSection = document.getElementById("hero-section");
  const bookingForm = document.getElementById("booking-form");
  const citiesSection = document.getElementById("cities");
  const hotelSelect = document.getElementById("hotel-select");
  const citySelect = document.getElementById("city-select");
  const tikectsInput = document.getElementById("tikects-input");
  const ticketPriceSpan = document.getElementById("ticket-price");
  const totalPriceSpan = document.getElementById("total-price");

  // get the selected country from localStorage
  const country = JSON.parse(localStorage.getItem("country"));

  // add content hero section
  heroSection.style.backgroundImage = `linear-gradient(rgba(0, 0, 0, 0.3), rgba(0, 0, 0, 0.3)),url(${country.country_hero_section_cover_img})`;
  heroSection.innerHTML = `
      <div class="flex-center" style="height: 100%">
        <h2 class="main-title">weclome to ${country.name}</h2>
      </div>
      `;

  //perevent deafult for booking form
  bookingForm.onsubmit = (event) => {
    event.preventDefault();
  };

  // getting cities from of country
  const cities = country.cities;
  // getting ticketPrice from of country
  const ticketPrice = country.ticketPrice;

  //empty citySelect and hotelSelect html elemnt To be assured that it is empty
  // then add defaultOption
  citySelect.innerHTML = " ";
  const defaultOption1 = document.createElement("option");
  defaultOption1.value = "default";
  defaultOption1.text = "Choose city";
  
  citySelect.append(defaultOption1);
  hotelSelect.innerHTML = " ";
  const defaultOption2 = document.createElement("option");
  defaultOption2.value = "default";
  defaultOption2.text = "Choose hotel";
  // map through cities to get each city
  // then create a new city div to display the city name and create an option in citySelect element
  cities.map((city) => {
    const cityDiv = document.createElement("div");
    cityDiv.classList.add("city");
    cityDiv.innerHTML = `
    <h3 class="cityTitle main-title">best hotels in ${city.city}</h3>
    `;
    // creating option its value and name  is the name of city and
    const cityOptions = document.createElement("option");
    cityOptions.value = city.city;
    cityOptions.textContent = city.city;
    citySelect.append(cityOptions);
    // getting hotels from city
    const hotels = city.hotels;
    // map through hotels to get each hotel
    // then create a new hotel div to display the hotels name,image and description  and create an option in hotelSelect element
    hotels.map((hotel) => {
      const hotelDiv = document.createElement("div");
      hotelDiv.classList.add("hotel", "flex-between", "flex-column-tablet");
      hotelDiv.innerHTML = `
      <div class="hotel-content-container width-40">
        <h4 class="hotel-name ">${hotel.name}</h4>
        <p class="hotel-description">${hotel.description}</p>
      </div>
      <div class="hotel-image-container  width-50">
      <img src=${hotel.img} alt=${hotel.name} class="max-width-100"  />
      </div>
      `;
      cityDiv.append(hotelDiv);
    });
    citiesSection.append(cityDiv);
  });

  citySelect.onchange = () => {
    const selectedCity = cities.find((city) => city.city === citySelect.value);
    const hotels = selectedCity.hotels;
    hotelSelect.innerHTML = " ";

    hotels.map(({ name }) => {
      // creatte hotel option
      const hotelOption = document.createElement("option");
      hotelOption.innerHTML = name;
      hotelOption.value = name;
      hotelSelect.append(hotelOption);
    });
  };

  tikectsInput.innerHTML = 0;
  tikectsInput.value = 0;
  totalPriceSpan.innerHTML = 0;
  ticketPriceSpan.innerHTML = ticketPrice;

  tikectsInput.onkeyup = () => {
    totalPriceSpan.innerHTML = tikectsInput.value * ticketPrice;
  };
  tikectsInput.onchange = () => {
    totalPriceSpan.innerHTML = tikectsInput.value * ticketPrice;
  };
}










